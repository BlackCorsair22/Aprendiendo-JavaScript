/* 📌 Tipos de datos que podemos guardar en las variables: 

    - String - Cadena de texto 
        - Existen 3 formas de declarar un string:

            - Comillas simples: 'Hola'
            - Comillas dobles: "Hola"
            - Comillas invertidas: `Hola`
        Estas tres formas son validas.
    

    - Number - Número
        - Existen 2 tipos de números:

            - Enteros: 1, 2, 3, 4, 5
            - Decimales: 1.5, 2.5, 3.5, 4.5, 5.5    
        ambos son validos para ser guardados en una variable.

    - Boolean - Verdadero o falso
        - Existen 2 valores booleanos:

            - Verdadero: true
            - Falso: false
        ambos son validos para ser guardados en una variable.

    - Array - Arreglo
        - Un arreglo es una colección de datos, que puede ser de cualquier tipo.
        - Se declaran con corchetes [] y los elementos se separan por comas.    
        - Ejemplo:
        
        const numeros = [1, 2, 3, 4, 5]


    object - Objeto
        - Un objeto es una colección de datos y/o funcionalidades.
        
        - Se declaran con llaves {} y los elementos se separan por comas.
        - Cada elemento del objeto se compone de una clave y un valor.
        - Ejemplo: 

        const persona = {
        nombre: 'Juan',
        edad: 30,
        ocupacion: 'Programador'}
        

    - function - Función
        - Una función es una subrutina que puede ser llamada por su nombre.
        - Se puede pasar datos a una función, llamados parámetros.
        - Una función puede devolver datos como resultado.
        - Ejemplo: 

        function suma(a, b) {
        return a + b;
        }

    - null - Valor nulo   
        - null es un valor especial que significa "nada
        - null es un valor asignado y representa la ausencia de valor.  

    - undefined - Valor indefinido
        - undefined es un valor especial que significa "no asignado"
        - undefined es el valor de una variable que no ha sido asignada.
*/
